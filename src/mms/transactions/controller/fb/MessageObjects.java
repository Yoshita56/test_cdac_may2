
package mms.transactions.controller.fb;

public class MessageObjects 
{
	private String Chk_ID 		;
	private String issueNo		;
	private String indentNo    ;
	private String issueDate 	;
	private String reqStoreName;
	private String crNO         ;
	private String patientName;
	
	
	public String getChk_ID() {
		return Chk_ID;
	}
	public void setChk_ID(String chk_ID) {
		Chk_ID = chk_ID;
	}
	public String getIssueNo() {
		return issueNo;
	}
	public void setIssueNo(String issueNo) {
		this.issueNo = issueNo;
	}
	public String getIndentNo() {
		return indentNo;
	}
	public void setIndentNo(String indentNo) {
		this.indentNo = indentNo;
	}
	public String getIssueDate() {
		return issueDate;
	}
	public void setIssueDate(String issueDate) {
		this.issueDate = issueDate;
	}
	public String getReqStoreName() {
		return reqStoreName;
	}
	public void setReqStoreName(String reqStoreName) {
		this.reqStoreName = reqStoreName;
	}
	public String getCrNO() {
		return crNO;
	}
	public void setCrNO(String crNO) {
		this.crNO = crNO;
	}
	public String getPatientName() {
		return patientName;
	}
	public void setPatientName(String patientName) {
		this.patientName = patientName;
	}
	
	
	
	
}
